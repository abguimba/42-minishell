#include "libft.h"
#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*c;

	c = s1;
	while (*c != '\0')
		c++;
	while (n-- > 0 && *s2 != '\0')
		*c++ = *s2++;
	*c = '\0';
	return (s1);
}
